package net.crytec.commands;

import co.aikar.commands.BaseCommand;
import co.aikar.commands.annotation.CommandAlias;
import co.aikar.commands.annotation.Default;
import co.aikar.commands.annotation.Description;
import org.bukkit.Bukkit;
import org.bukkit.entity.Player;
import org.bukkit.event.inventory.InventoryType;
import org.bukkit.inventory.Inventory;

@CommandAlias("müll")
@Description("Öffnet ein Kiste in der du deine Items löschen kannst.")
public class Trashbin extends BaseCommand {

	@Default
	public void openTrashbin(final Player sender) {
		final Inventory trash = Bukkit.createInventory(sender, InventoryType.CHEST, "Mülleimer");
		sender.openInventory(trash);
	}
}